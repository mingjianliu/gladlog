export type TrinketType = 'Gladiator' | 'Adaptation' | 'Relentless' | 'Unknown';

export interface ICCInstance {
  atSeconds: number;
  durationSeconds: number;
  spellId: string;
  spellName: string;
  sourceName: string;
  sourceSpec: string;
  damageTakenDuring: number;
  trinketState: 'used' | 'available_unused' | 'on_cooldown' | 'passive_trinket';
  /** Seconds until trinket is ready again. Only populated when trinketState === 'on_cooldown'. */
  trinketCDSecondsLeft?: number;
  /** DR state at the time this CC was applied. null if spell not in DR category map. */
  drInfo: IDRInfo | null;
  /**
   * Distance in yards between caster and target at the moment of CC application.
   * null when advanced logging is absent.
   */
  distanceYards: number | null;
  /**
   * Whether the caster had unobstructed line of sight to the target.
   * null when arena geometry is not mapped or advanced logging is absent.
   */
  losBlocked: boolean | null;
}

export interface IRootInstance {
  atSeconds: number;
  durationSeconds: number;
  spellId: string;
  spellName: string;
  sourceName: string;
  sourceSpec: string;
}

export interface IInterruptInstance {
  atSeconds: number;
  lockoutDurationSeconds: number;
  kickSpellId: string;
  kickSpellName: string;
  interruptedSpellId: string;
  interruptedSpellName: string;
  sourceName: string;
  sourceSpec: string;
}

export interface ICCAvoidedInstance {
  atSeconds: number;
  spellId: string;
  spellName: string;
  avoidanceSpellName: string;
  avoidanceSpellId: string;
  sourceName: string;
  sourceSpec: string;
}

export interface IPlayerCCTrinketSummary {
  playerName: string;
  playerSpec: string;
  trinketType: TrinketType;
  trinketCooldownSeconds: number;
  ccInstances: ICCInstance[];
  trinketUseTimes: number[]; // atSeconds for each trinket cast
  /** CC windows where trinket was available but player didn't use it, with significant damage */
  missedTrinketWindows: ICCInstance[];
  /** Roots applied by enemies. No DR category. */
  rootInstances: IRootInstance[];
  /** Disarms applied by enemies. No DR category. */
  disarmInstances: IRootInstance[];
  /** Kicks (interrupts) landed by enemies. */
  interruptInstances: IInterruptInstance[];
  /** CC avoidance/mitigation/breaks. Always populated by analyzePlayerCCAndTrinket
   * (empty array when none) — part of the summary contract. */
  ccAvoidedInstances: ICCAvoidedInstance[];
}

